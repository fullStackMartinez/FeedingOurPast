		<p><strong>Attitudes and Behaviors</strong>: Diane is tired and busy with her full-time teaching job during the day, and then when not shuttling her kids to extracurricular activities in the evening, she is home preparing dinner, catching up on housework, helping her kids with homework, and grading her student's schoolwork. She doesn't get a lot of time on her computer at home and relies on her phone a lot to quickly search for information online.</p>
		<p><strong>Needs and Goals</strong>: Diane has recently learned that her elderly neighbor's wife died and he's now living alone with no family in town. She offers him meals when she is able to (maybe twice per week), but she worries he is not eating properly or getting out of the house to shop for food. She does not know his financial state and would like to find help for him, particularly a local food pantry to keep him from going hungry.</p>
		<p><strong>User Story</strong>: As a user, I need to be able to quickly find food assistance in my area for my elderly neighbor.</p>
